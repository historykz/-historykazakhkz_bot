def get_text(key: str, lang: str, **kwargs) -> str:
    """Return localised string. Falls back to 'ru' if lang unknown."""
    lang = lang if lang in ("ru", "kz") else "ru"
    entry = T.get(key, {})
    text = entry.get(lang) or entry.get("ru") or f"[{key}]"
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
    return text
